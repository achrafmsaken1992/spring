package org.sid.form;

public class NoteForm {
	Long id;
int reponseC;
int reponseF;
Long etudiantId;
Long qcmId;
public Long getId() {
	return id;
}
public void setId(Long id) {
	this.id = id;
}
public int getReponseC() {
	return reponseC;
}
public void setReponseC(int reponseC) {
	this.reponseC = reponseC;
}
public int getReponseF() {
	return reponseF;
}
public void setReponseF(int reponseF) {
	this.reponseF = reponseF;
}
public Long getEtudiantId() {
	return etudiantId;
}
public void setEtudiantId(Long etudiantId) {
	this.etudiantId = etudiantId;
}
public Long getQcmId() {
	return qcmId;
}
public void setQcmId(Long qcmId) {
	this.qcmId = qcmId;
}
	
	
}
